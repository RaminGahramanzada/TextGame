package bean;

/**
 *
 * @author Lenovo
 */
public abstract class Person {

    private String name;
    private String surname;

    Person() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSurname() {
        return surname;
    }

    public void setSurname(String surname) {
        this.surname = surname;
    }

}
